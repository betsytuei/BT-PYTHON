# Arithmetic operators - Perform simple calculations

x = 67
y = 2

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) # Modulus - Returns the remainder

#Comparison operators
a = 56
b = 21
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b) # Equal to
print(a != b) # Not Equal to

# Assignment operators

z = 20
print(z)

num = 2
num += 23
print(num)

print(15 - 6 + 2 * 6)

# Logical Operators
